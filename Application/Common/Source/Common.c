/*
*----------------------------------------------------------------------
*  Name     : CalculateChecksum_Fletcher16
*
*  Summary  : This function calculate checksum using Fletcher16 algorithm
*
*  Input    : void
*
*  Output   : void
*
*  Returns  : Calculated checksum
*
*  Notes    : NA
*
*----------------------------------------------------------------------
*/
UINT16 CalculateChecksum_Fletcher16(UINT8 *pData, UINT16 Count)
{
   UINT16 Sum_1 = 0;
   UINT16 Sum_2 = 0;
   UINT16 index;

   for( index = 0; index < Count; ++index )
   {
      Sum_1 = (Sum_1 + pData[index]) % 255;
      Sum_2 = (Sum_2 + Sum_1) % 255;
   }

   return (Sum_2 << 8) | Sum_1;
}

/*
*----------------------------------------------------------------------
*  Name     : CalculateChecksum_Fletcher32
*
*  Summary  : This function calculate checksum using Fletcher32 algorithm
*
*  Input    : void
*
*  Output   : void
*
*  Returns  : Calculated checksum
*
*  Notes    : NA
*
*----------------------------------------------------------------------
*/
UINT32 CalculateChecksum_Fletcher32(const UINT16 *pData, UINT32 Count)
{
   UINT32 c0, c1;
   UINT32 i;

   for (c0 = c1 = 0; Count >= 360; Count -= 360)
   {
      for (i = 0; i < 360; ++i)
      {
         c0 = c0 + *pData++;
         c1 = c1 + c0;
      }
      c0 = c0 % 65535;
      c1 = c1 % 65535;
   }

   for (i = 0; i < Count; ++i)
   {
      c0 = c0 + *pData++;
      c1 = c1 + c0;
   }

   c0 = c0 % 65535;
   c1 = c1 % 65535;

   return (c1 << 16 | c0);
}

/*
*----------------------------------------------------------------------
*  Name     : CountBitInbytes
*
*  Summary  :
*
*  Input    : void
*
*  Output   : void
*
*  Returns  :
*
*  Notes    : NA
*
*----------------------------------------------------------------------
*/
UINT16 CountBitInbytes(void *pBuff, UINT16 NoOfBytes)
{
   UINT16 BitsCount = 0;
   UINT16 Index, BitIndex;
   UINT8  *pBytes = (UINT8*)pBuff;

   if(pBuff != NULL)
   {
      for(Index = 0; Index < NoOfBytes; Index++)
      {
         for(BitIndex = 0; BitIndex < 8; BitIndex++)
         {
            if(pBytes[Index] & (0x01 << BitIndex))
            {
               BitsCount++;
            }
         }
      }
   }

   return BitsCount;
}
/*
*----------------------------------------------------------------------
*  Name     : TrimWhiteSpaceInString
*
*  Summary  :
*
*  Input    : void
*
*  Output   : void
*
*  Returns  :
*
*  Notes    : NA
*
*----------------------------------------------------------------------
*/
UCHAR *TrimWhiteSpaceInString(UCHAR *str)
{
   UCHAR *end;

  // Trim leading space
  while(isspace((UCHAR)*str)) str++;

  if(*str == 0)  // All spaces?
    return str;

  // Trim trailing space
  end = str + strlen(str) - 1;

  while(end > str && isspace((unsigned char)*end)) end--;

  // Write new null terminator character
  end[1] = '\0';

  return str;
}
/*
*----------------------------------------------------------------------
*  Name     : TrimWhiteSpaceInString
*
*  Summary  :
*
*  Input    : void
*
*  Output   : void
*
*  Returns  :
*
*  Notes    : NA
*
*----------------------------------------------------------------------
*/
void ExtractSubString(UCHAR *pInput, UCHAR *pOutput, UCHAR StartChar, UCHAR EndChar, UINT16 BuffSize)
{
   UINT16  StartIndex = 0, EndIndex = 0, Index = 0;;

   if((pInput != NULL) && (pOutput != NULL))
   {
      memset(pOutput, 0x00, BuffSize);

      // check all characters
      while((pInput[Index]) != 0)
      {
         if((pInput[Index]) == StartChar)
         {
            StartIndex = Index + 1;
         }

         if((pInput[Index]) == EndChar)
         {
            EndIndex = Index;
            break;
         }

         // increment input string
         Index++;
      }

      if((EndIndex > StartIndex) &&
         ((EndIndex - StartIndex) < BuffSize))
      {
         strncpy(pOutput, &pInput[StartIndex], (EndIndex - StartIndex));
      }
   }
}
