t_Status GetProtocolIDandAddressFromString(const CHAR *pDeviceString, t_Protocol *pProtocol, UINT16 *pAddress)
{
   t_Status ReturnValue = STS_OK;

   // Extract protocol string from device string
   CHAR ProtocolString[40];
   CHAR DeviceAddressString[40];
   UINT16 Index = 0;
   UINT16 Length = strlen(pDeviceString);

   memset(ProtocolString, 0x00, sizeof(ProtocolString));
   memset(DeviceAddressString, 0x00, sizeof(DeviceAddressString));

   // extract sub string till '_' and store in ProtocolString, from 0th indet to '_' character
   for(Index = 0; Index < Length && Index < sizeof(ProtocolString) - 1; Index++)
   {
      if(pDeviceString[Index] == '_')
      {
         ProtocolString[Index] = '\0';
         break;
      }
      else
      {
         ProtocolString[Index] = pDeviceString[Index];
      }
   }

   // copy deevice address string from device string to DeviceAddressString, from '_' character to end of string
   if(Index < Length)
   {
      strncpy(DeviceAddressString, &pDeviceString[Index + 1], Length - Index - 1);

      DeviceAddressString[Length - Index - 1] = '\0';

      // convert device address string to integer
      *pAddress = (UINT16)atoi(DeviceAddressString);
   }
   else
   {
	   *pAddress = 0xFFFF; // if address is not present in string, assign invalid address
   }

   // now compare extracted protocol string with known protocol strings and get protocol ID
   if(strcmp(ProtocolString, "MODBUS") == 0)
   {
      *pProtocol = MODBUS;
   }
   else if(strcmp(ProtocolString, "CAN") == 0)
   {
      *pProtocol = CAN;
   }
   else if(strcmp(ProtocolString, "SERIAL_ASCII") == 0)
   {
      *pProtocol = SERAIL_ASCII;
   }
   else
   {
      ReturnValue = STS_ERROR_MODULENOTDEFINED;
   }

   return ReturnValue;
}
